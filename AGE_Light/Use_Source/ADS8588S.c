void ADS8588SReadOneSample(uint16_t *iRdData)
{
    uint16_t  i = 0,j=0;
    uint16_t  Temp = 0;

    ADS8588SReset();
    ADS8588SCONV_LOW;
    DELAY_Ms(10);
    ADS8588SCONV_HIGH;
    DELAY_Ms(500);
    ADS8588SCS_LOW;
    DELAY_Ms(5);
    for(i=0; i<128; i++)  //64  --- -4
    {
        j=i/16;
        ADS8588SCLK_LOW;
        DELAY_Ms(1);
        Temp = ADS8588SDOA;
        iRdData[j] <<= 1;
        if(Temp)
        {
            iRdData[j] |= 1;
        }
        ADS8588SCLK_HIGH;
        DELAY_Ms(1);
    }
    DELAY_Ms(2);
    ADS8588SCS_HIGH;
    DELAY_Ms(2);
}




